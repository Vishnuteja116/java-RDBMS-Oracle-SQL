--
-- Table structure for table `regions`
--
CREATE TABLE REGIONS(
  REGION_ID number(5) PRIMARY KEY,
  REGION_NAME varchar2(25)UNIQUE
);
--
-- Table structure for table `countries`
--
CREATE TABLE countries(
  COUNTRY_ID varchar(2)PRIMARY KEY,
  COUNTRY_NAME varchar(40) DEFAULT NULL,
  REGION_ID NUMBER(5) DEFAULT NULL,
  CONSTRAINT COUNTR_REG_FK FOREIGN KEY(REGION_ID) REFERENCES REGIONS(REGION_ID)
);
--
-- Table structure for table `locations`
--

CREATE TABLE locations(
  LOCATION_ID number(4) PRIMARY KEY,
  STREET_ADDRESS varchar(40) DEFAULT NULL,
  POSTAL_CODE varchar(12) DEFAULT NULL,
  CITY varchar(30) NOT NULL,
  STATE_PROVINCE varchar(25) DEFAULT NULL,
  COUNTRY_ID varchar(2) DEFAULT NULL,
  --CONSTRAINT LOC_CITY_IX FOREIGN KEY(CITY) REFERENCES REGIONS(CITY),
  CONSTRAINT LOC_COUNTRY_IX FOREIGN KEY(COUNTRY_ID) REFERENCES countries(COUNTRY_ID)
  --CONSTRAINT LOC_STATE_PROVINCE_IX FOREIGN KEY(STATE_PROVINCE) REFERENCES REGIONS(STATE_PROVINCE)
);
--
-- Table structure for table `jobs`
--
CREATE TABLE jobs(
  JOB_ID varchar(10) PRIMARY KEY,
  JOB_TITLE varchar(35) NOT NULL,
  MIN_SALARY number(6) DEFAULT NULL,
  MAX_SALARY number(6) DEFAULT NULL
);
--
-- Table structure for table `departments`
--

CREATE TABLE departments(
  DEPARTMENT_ID number(4) PRIMARY KEY,
  DEPARTMENT_NAME varchar(30) NOT NULL,
  MANAGER_ID number(6) DEFAULT NULL,
  LOCATION_ID number(4) DEFAULT NULL,
  CONSTRAINT DEPT_LOCATION_IX FOREIGN KEY (LOCATION_ID) REFERENCES  locations(LOCATION_ID)
);

--
-- Table structure for table `employees`
--

CREATE TABLE employees(
  EMPLOYEE_ID number(6) PRIMARY KEY,
  FIRST_NAME varchar(20) DEFAULT NULL,
  LAST_NAME varchar(25) NOT NULL,
  EMAIL varchar(25) NOT NULL UNIQUE,
  PHONE_NUMBER varchar(20) DEFAULT NULL,
  HIRE_DATE date NOT NULL,
  JOB_ID varchar(10) NOT NULL,
  SALARY number(8,2) DEFAULT NULL,
  COMMISSION_PCT number(2,2) DEFAULT NULL,
  MANAGER_ID number(6) DEFAULT NULL,
  DEPARTMENT_ID number(4) DEFAULT NULL,
  CONSTRAINT EMP_DEPARTMENT_IX FOREIGN KEY(DEPARTMENT_ID) REFERENCES departments(DEPARTMENT_ID),
  CONSTRAINT EMP_JOB_IX FOREIGN KEY(JOB_ID) REFERENCES jobs(JOB_ID),
  CONSTRAINT EMP_MANAGER_IX FOREIGN KEY(MANAGER_ID) REFERENCES employees(EMPLOYEE_ID)
  --CONSTRAINT EMP_NAME_IX FOREIGN KEY(REGION_ID) REFERENCES REGIONS(LAST_NAME,FIRST_NAME)
);

--
-- Table structure for table `job_history`
--
CREATE TABLE job_history (
  EMPLOYEE_ID NUMBER(6),
  START_DATE date NOT NULL,
  END_DATE date NOT NULL,
  JOB_ID varchar(10) NOT NULL,
  DEPARTMENT_ID number(4) DEFAULT NULL,
  CONSTRAINT JOB_HISTORY_PK PRIMARY KEY(EMPLOYEE_ID,START_DATE),
  CONSTRAINT JHIST_DEPARTMENT_IX FOREIGN KEY(DEPARTMENT_ID) REFERENCES departments(DEPARTMENT_ID),
  CONSTRAINT JHIST_EMPLOYEE_IX FOREIGN KEY(EMPLOYEE_ID) REFERENCES employees(EMPLOYEE_ID),
  CONSTRAINT JHIST_JOB_IX FOREIGN KEY(JOB_ID) REFERENCES jobs(JOB_ID)
);


